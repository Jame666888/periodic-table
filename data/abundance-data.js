// 如果元素在某环境中无数据，返回 null
function getAbundance(z, environment) {
  const envKey = environment; // 'crust' | 'solar' | 'human'
  const data = ABUNDANCE_DATA[envKey];
  if (!data) return null;
  const val = data[z];
  if (val === 0 || val === undefined) return null;
  return val;
}

// 格式化丰度数值（自动选择单位）
function formatAbundance(val, environment) {
  if (val === null || val === undefined) return "无数据";

  if (environment === 'solar') {
    // 太阳系丰度：科学计数法
    if (val >= 1e6) return val.toExponential(2).replace('e+', '×10^');
    if (val >= 1000) return (val / 1e6).toFixed(2) + '×10⁶';
    return val.toFixed(1);
  }

  // 地壳和人体：ppm
  if (val >= 10000) {
    return (val / 10000).toFixed(2) + '%';  // 转换为百分比
  }
  if (val >= 1) {
    return val.toFixed(1) + ' ppm';
  }
  if (val >= 0.001) {
    return (val * 1000).toFixed(1) + ' ppb';
  }
  return val.toExponential(2) + ' ppm';
}
